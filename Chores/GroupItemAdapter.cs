
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

using Android.App;
using Android.Content;
using Android.OS;
using Android.Runtime;
using Android.Util;
using Android.Views;
using Android.Widget;

namespace Chores
{
	public class GroupItemAdapter : BaseAdapter<GroupInfo>
	{
		List<GroupInfo> items;
		Activity context;

		public GroupItemAdapter(Activity context, List<GroupInfo> items) : base()
		{
			this.context = context;
			this.items = items;
		}

		public override long GetItemId(int position)
		{
			return position;
		}

		public override GroupInfo this[int position]
		{
			get { return items [position]; }
		}

		public override int Count
		{
			get { return items.Count; }
		}

		public override View GetView(int position, View convertView, ViewGroup parent)
		{
			var item = items [position];

			View view = convertView;
			if (view == null) {
				view = context.LayoutInflater.Inflate (Resource.Layout.GroupListItem, null);
			}

			view.FindViewById<TextView> (Resource.Id.txtGroupName).Text = item.groupName;

			return view;
		}
	}
}